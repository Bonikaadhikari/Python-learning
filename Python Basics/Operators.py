a = 10
b = 5

ans1 = (a + b) 
print("Addition of", a, "and", b, "is", ans1)

ans2 = (a - b)
print("Subtraction of", a, "and", b, "is", ans2)

ans3 = (a * b)
print("Multiplication of", a, "and", b, "is", ans3)

ans4 = (a / b)
print("Division of", a, "and", b, "is", ans4)

ans5 = (a % b)
print("Modulus of", a, "and", b, "is", ans5)

ans6 = (a // b)
print("FloorDivision of", a, "and", b, "is", ans6)
#FloorDivision print the value without decimal value

ans7 = (a ** b)
print("Exponential of", a, "times", b, "is", ans7)
# ** helps to give power of some numbers with other
